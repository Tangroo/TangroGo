package calc

// Add() is a function of two additions.
func Add(x, y int) int {
	return x + y
}

// Sub() is a function of tow subtract.
func Sub(x, y int) int {
	return x - y
}

// Mul() is a function of two multiplication.
func Mul(x, y int) int {
	return x * y
}

// Div() is a function of tow division.
func Div(x, y int) int {
	return x / y
}
